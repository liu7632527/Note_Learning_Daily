public class FirstValues {
    static byte a;
    static short b;
    static int c;
    static long d;
    static float e;
    static double f;
    static char g;
    static boolean h;
    public static void main(String[] args){
        System.out.println("变量默认值如下\n"+
                "byte: "+ a + "\n" +
                "short: "+ b + "\n" +
                "int: "+ c + "\n" +
                "long: "+ d + "\n" +
                "float: "+ e + "\n" +
                "double: "+ f + "\n" +
                "char: "+ g + "\n" +
                "boolean: "+ h + "\n");
    }
}
